export type TranslationSet = (typeof translations)[Language];

export function isLanguage(value: string | null | undefined): value is Language {
  return Boolean(value && languages.includes(value as Language));
}

export function getStatusLabel(status: string, language: Language): string {
  const normalized = status.trim().toUpperCase();
  return statusLabels[language][normalized] ?? statusLabels[language].UNKNOWN;
}

export function formatTrackText(template: string, values: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? ""));
}
